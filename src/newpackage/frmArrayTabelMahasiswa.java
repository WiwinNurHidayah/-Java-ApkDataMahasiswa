     class MyList implements ListSelectionListener {

        public void valueChanged(ListSelectionEvent e) {

            if (e.getValueIsAdjusting()) {
                return;
            } 

            ListSelectionModel lsm = (ListSelectionModel) e.getSource();
            selectedRow = lsm.getMinSelectionIndex();
            TableModel tb = tblHasil.getModel();

            tfNim.setText(tb.getValueAt(selectedRow, 0).toString());
            tfNama.setText(tb.getValueAt(selectedRow, 1).toString());
            tfJalur.setText(tb.getValueAt(selectedRow, 2).toString());
            tfAsal.setText(tb.getValueAt(selectedRow, 3).toString());
            tfNomer.setText(tb.getValueAt(selectedRow, 4).toString());
            tfNim.setEditable(false);
        }
    }
 
    void pilih() {
        tblHasil.setSelectionMode(ListSelectionModel.SINGLE_SELECTION); 
        ListSelectionModel rowSM = tblHasil.getSelectionModel();
        rowSM.addListSelectionListener(new MyList());

    }
 
    final void viewDataMahasiswa() {
        index = 0; 
        DefaultTableModel model;
        model = (DefaultTableModel) tblHasil.getModel();

        DBConnect connect = new DBConnect();
        Object[][] data = connect.getDataMahasiswa();
        if (model.getRowCount() == 0) {
            Object[] row;
            for (int i = 0; i < connect.getCountMahasiswa(); i++) {
                row = new Object[]{data[i][0], data[i][1], data[i][2], data[i][3], data[i][4]};
                model.addRow(row);
                index++;
            }
        }
    }


