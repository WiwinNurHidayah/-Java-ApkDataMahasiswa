    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        String nim = tfNim.getText();
        String nama = tfNama.getText();
        String jalurMasuk = tfJalur.getText();
        String asalDaerah = tfAsal.getText();
        String noHp = tfNomer.getText();
        tfNim.setText("");
        tfNama.setText("");
        tfJalur.setText("");
        tfAsal.setText("");
        tfNomer.setText("");
        DBConnect connect = new DBConnect();
        connect.setDataMahasiswa(nim, nama, jalurMasuk, asalDaerah, noHp);
        DefaultTableModel model;
        model = (DefaultTableModel) tblHasil.getModel();
        Object[] row;
        row = new Object[]{nim, nama, jalurMasuk, asalDaerah, noHp};
        model.addRow(row);   
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb;
        tb = (DefaultTableModel) tblHasil.getModel();

        String nim = tfNim.getText();
        String nama = tfNama.getText();
        String jalurMasuk = tfJalur.getText();
        String asalDaerah = tfAsal.getText();
        String noHp = tfNomer.getText();
        tb.setValueAt(nim, selectedRow, 0);
        tb.setValueAt(nama, selectedRow, 1);
        tb.setValueAt(jalurMasuk, selectedRow, 2);
        tb.setValueAt(asalDaerah, selectedRow, 3);
        tb.setValueAt(noHp, selectedRow, 4);
        DBConnect connect = new DBConnect();
        connect.updateMahasiswa(nama, jalurMasuk, asalDaerah, noHp, tb.getValueAt(selectedRow,0).toString());
        
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tb;
        tb = (DefaultTableModel) tblHasil.getModel();
        String nm = (String) tb.getValueAt(selectedRow, 0); 
        
        for (int i = selectedRow; i < index-1; i++) {
            tb.setValueAt(tb.getValueAt(i + 1, 0), i, 0);
            tb.setValueAt(tb.getValueAt(i + 1, 1), i, 1);
            tb.setValueAt(tb.getValueAt(i + 1, 2), i, 2);
            tb.setValueAt(tb.getValueAt(i + 1, 3), i, 3);
            tb.setValueAt(tb.getValueAt(i + 1, 4), i, 4);
        }

        tb.setValueAt("", index-1, 0);
        tb.setValueAt("", index-1, 1);
        tb.setValueAt("", index-1, 2);
        tb.setValueAt("", index-1, 3);
        tb.setValueAt("", index-1, 4);
        index--;
        
        tfNim.setText("");
        tfNama.setText("");
        tfJalur.setText("");
        tfAsal.setText("");
        tfNomer.setText("");

        DBConnect connect = new DBConnect();
        connect.deleteMahasiswa(nm);
       
    }//GEN-LAST:event_btnHapusActionPerformed

